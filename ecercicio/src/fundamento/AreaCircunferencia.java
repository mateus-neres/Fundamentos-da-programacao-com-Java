package fundamento;

public class AreaCircunferencia {
	public static void main(String[] args) {
		
		// Variaveis
		double raio = 3.4;
		
		// Constantes
		final double pi = 3.14159;
		
		// Tratamento dos dados
		double area = pi * (raio * raio);
		
		// Saida de dados
		System.out.println(area);
	}
}
